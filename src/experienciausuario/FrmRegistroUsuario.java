    private void BtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtGuardarActionPerformed

        boolean nombre = metodos.ValidarLetras(txtNombres.getText());
        boolean apellido = metodos.ValidarLetras(txtApellidos.getText());

        if (txtNombres.getText().equals("") || txtApellidos.getText().equals("") || txtCorreo.getText().equals("") || txtUsuario.getText().equals("") || String.valueOf(txtContrasena.getPassword()).equals("") ){
            JOptionPane.showMessageDialog(null, "Faltan campos por llenar ");
        }else{
            if (metodos.ValidarUsuarioRepetido(txtUsuario.getText())==0 ){

                metodos.guardar(txtNombres.getText(),txtApellidos.getText(), txtCorreo.getText(), txtUsuario.getText(), String.valueOf(txtContrasena.getPassword()));
                JOptionPane.showMessageDialog(null, "guardado ");

            } else{
                JOptionPane.showMessageDialog(null, "el usuario ya existe ");
            }
        }

    }//GEN-LAST:event_BtGuardarActionPerformed

